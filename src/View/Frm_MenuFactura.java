    private void cargarTable() {
        tabla.setLista(controlador.listar());
        for (int i = 0; i < jTFacturas.getColumnCount(); i++) {
            jTFacturas.setModel(tabla);
        }
    }

    private void buscar() {
        String codigo = JTcodigoFactura.getText();
        controlador.buscarFactura(codigo);
    }

