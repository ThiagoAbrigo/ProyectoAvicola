    private void imprimir() {
        int indice = jTFacturas.getSelectedColumn();
        try {
            File path = new File("carpeta/" + indice + ".pdf");
            Desktop.getDesktop().open(path);
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }

