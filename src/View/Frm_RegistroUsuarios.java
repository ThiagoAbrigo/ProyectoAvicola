    private void guardarregistro() {
        if (txtusuario.getText().isEmpty() || password.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {
            registroDAO.getRegistro().setUser(txtusuario.getText());
            registroDAO.getRegistro().setPassword(password.getText());
            if (registroDAO.guardar()) {
                JOptionPane.showMessageDialog(null, "Registrado exitosamente");
                Login login = new Login();
                login.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar");
            }
        }
    }

